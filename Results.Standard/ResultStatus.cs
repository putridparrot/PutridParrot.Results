namespace PutridParrot.Results
{
    /// <summary>
    /// A return/result status
    /// </summary>
    public enum ResultStatus
    {
        /// <summary>
        /// A default, unset/undefined value
        /// </summary>
        Undefined,
        /// <summary>
        /// Indicates a result/return was successful
        /// </summary>
        Success,
        /// <summary>
        /// Indicates a result/return was a failure
        /// </summary>
        Failure
    }
}